window.renderCategories = function(targetId){
  const wrap = document.getElementById(targetId);
  if(!wrap) return;
  wrap.innerHTML = '';
  CATEGORIES.forEach(cat=>{
    const card = document.createElement('div');
    card.className='card';
    card.innerHTML = `<strong>${cat.name}</strong>` + (cat.children? `<div class="label">${cat.children.map(c=>c.name).join(' • ')}</div>`:'');
    card.addEventListener('click', ()=>{
      window.location.href = `./products.html?cat=${encodeURIComponent(cat.id)}`;
    });
    wrap.appendChild(card);
  });
}
